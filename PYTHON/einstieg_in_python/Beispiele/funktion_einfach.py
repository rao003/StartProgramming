# Definition der Funktion
def stern():
    print("-----------------")
    print("*** Trennung ****")
    print("-----------------")

# Haupt-Programm
x = 12
y = 5
stern()                         # 1. Aufruf
print("x =", x, ", y =", y)
stern()                         # 2. Aufruf
print("x + y =", x + y)
stern()                         # 3. Aufruf
print("x - y =", x - y)
stern()                         # 4. Aufruf




