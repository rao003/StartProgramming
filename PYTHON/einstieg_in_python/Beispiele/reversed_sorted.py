# Zeichenkette
z = "Robinson"
print(z)
r = reversed(z)
for x in r:
    print(x, end=" ")
print()
s = sorted(z)
print(s)
print()

# Tupel
t = 4, 12, 6, -2
print(t)
r = reversed(t)
for x in r:
    print(x, end=" ")
print()
s = sorted(t)
print(s)
print()

